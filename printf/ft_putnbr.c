#include "ft_printf.h"

int	ft_putnbr(int n)
{
	char	a;

	a = 0;
	if (n < 0)
	{
		if (n == -2147483648)
		{
			write(1, "-2147483648", 11);
			return (11);
		}
		n *= -1;
		write(1, "-", 1);
	}
	if (n < 10)
	{
		a = n + '0';
		write(1, &a, 1);
	}
	else
	{
		ft_putnbr_fd(n / 10);
		ft_putnbr_fd(n % 10);
	}
}
